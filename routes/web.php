Route::get('/', function () {
    return view('welcome');
});
Route::post('/loadfile', [FileController::class, 'loadFile'])->middleware('auth');
Route::get('/dashboard', [FileController::class, 'showDashboard'])->middleware('auth')->name('dashboard');
Route::get('/downloadFile/{id}', [FileController::class, 'downloadFile'])->middleware('auth');
Route::middleware('auth')->group(function () {
    Route::get('/profile', [ProfileController::class, 'edit'])->name('profile.edit');
    Route::patch('/profile', [ProfileController::class, 'update'])->name('profile.update');
    Route::delete('/profile', [ProfileController::class, 'destroy'])->name('profile.destroy');
});

